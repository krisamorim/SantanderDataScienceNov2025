word = list("python") # "p", "y", "t", "h", "o", "n"
print(word[1:4])  # ['y', 't', 'h']
print(word[0:3:2])  # ['p', 't'] (zero to 3 skipping 2)
print(word[2:])   # ['t', 'h', 'o', 'n']
print(word[:2])   # ['p', 'y']
print(word[-4:-1]) # ['h', 'o', 'n']
print(word[:])    # ['p', 'y', 't', 'h', 'o', 'n']
print(word[-3:])  # ['h', 'o', 'n']
