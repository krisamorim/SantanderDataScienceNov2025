name = "Silvio Santos"
print(name[0])  # S (letter with index 0)
print(name[:6])  # Silvio (from index 0 to index 5)
print(name[7:])  # Santos (from index 7 to the end)
print(name[::2])  # Sli oas (every second letter)
print(name[1::2])  # iv ants (every second letter starting from index 1)
print(name[::-1])  # sotnaS ovilS (the string reversed)
print(name[-1])  # s (last letter)
print(name[-6:])  # Santos (last 6 letters)
print(name[:-7])  # Silvio (all but the last 7 letters)
