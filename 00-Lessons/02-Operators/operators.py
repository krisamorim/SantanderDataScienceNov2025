#Operators arithmetic examples
print('Sum: 1+1 is equal to', 1+1)

print('Minus: 1-1 is equal to', 1-1)

print('Multiply: 1*9 is equal to', 1*9)

print('Modulus: 10%3 is equal to', 10%3)

print('Division: 10/2 is equal to', 10/2)

print('Exponentiation: 10**3 is equal to', 10**3)

#Parenteses -> Exponentiation -> Multiplication/Division/Modulus -> Addition/Subtraction
print('Complex operation: 10 + 3 * 2 ** 2 is equal to', 10 + 3 * 2 ** 2)

print('-'*60)

#Operators comparison examples
print('Is 1 equal to 1?', 1 == 1)
print('Is 1 different from 2?', 1 != 2)
print('Is 3 greater than 2?', 3 > 2)
print('Is 2 less than 3?', 2 < 3)
print('Is 2 greater than or equal to 2?', 2 >= 2)
print('Is 3 less than or equal to 2?', 3 <= 2)
print('Is 1 equal to "1"?', 1 == '1')
print('Is 1 identical to "1"?', 1 is '1')
print('Is 1 not identical to "1"?', 1 is not '1')
print('-'*60)

