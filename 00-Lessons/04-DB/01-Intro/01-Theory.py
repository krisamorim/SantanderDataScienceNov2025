#Types of databases
#1. Relational Databases (RDBMS)
#   - Store data in tables with rows and columns.
#   - Use Structured Query Language (SQL) for data manipulation.
#   - Examples: MySQL, PostgreSQL, Oracle Database, Microsoft SQL Server.

#2. NoSQL Databases
#   - Designed for unstructured or semi-structured data.
#   - Types include document stores, key-value stores, wide-column stores, and graph databases.
#   - Examples: MongoDB (document store), Redis (key-value store), Cassandra (wide-column store), Neo4j (graph database).

#3. In-Memory Databases
#   - Store data in the main memory (RAM) for faster access.
#   - Used for caching and real-time applications.
#   - Examples: Redis, Memcached.

#4. NewSQL Databases
#   - Combine the scalability of NoSQL with the ACID properties of traditional RDBMS.
#   - Designed to handle high transaction rates while maintaining consistency.
#   - Examples
#   - Google Spanner, CockroachDB, VoltDB.

#5. Time-Series Databases
#   - Optimized for storing and querying time-stamped data.
#   - Commonly used in IoT, finance, and monitoring applications.
#   - Examples: InfluxDB, TimescaleDB, OpenTSDB.

#6. Object-Oriented Databases
#   - Store data in the form of objects, similar to object-oriented programming.
#   - Allow for complex data representations and relationships.
#   - Examples: db4o, ObjectDB.

#7. Graph Databases
#   - Designed to store and query data represented as graphs (nodes and edges).
#   - Ideal for applications involving relationships, such as social networks and recommendation systems.
#   - Examples: Neo4j, Amazon Neptune, ArangoDB.

#8. Hierarchical Databases
#   - Organize data in a tree-like structure with parent-child relationships.
#   - Used in applications where data has a clear hierarchy.
#   - Examples: IBM Information Management System (IMS), Windows Registry.

#9. Network Databases
#   - Use a flexible model that allows multiple parent-child relationships.
#   - Suitable for complex data relationships.
#   - Examples: Integrated Data Store (IDS), Raima Database Manager.

#10. Cloud Databases
#   - Hosted on cloud platforms, providing scalability and accessibility.
#   - Can be relational or NoSQL.
#   - Examples: Amazon RDS, Google Cloud Spanner, Microsoft Azure Cosmos DB.

#11. Distributed Databases
#   - Spread data across multiple physical locations for redundancy and performance.
#   - Can be relational or NoSQL.
#   - Examples: Apache Cassandra, Google Bigtable, CockroachDB.

#12. Embedded Databases
#   - Integrated into applications for local data storage.
#   - Lightweight and require minimal setup.
#   - Examples: SQLite, Berkeley DB.

