#installing docker by powershell commands
#1- Open powershell in admin mode and run the following command to install Docker
# winget install Docker.DockerDesktop

#2- After installation, you may need to log out and log back in or restart your computer to ensure Docker is properly set up.

#3- Once Docker is installed, you can start Docker Desktop from the Start menu.

#4- Follow the on-screen instructions to complete the setup, including enabling WSL 2 integration if prompted.

#5- After setup, you can verify the installation by opening a new terminal and running the command:
# docker --version
# This should display the installed version of Docker.